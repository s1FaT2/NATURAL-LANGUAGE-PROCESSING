import nltk
import re
import math
import random
import string
from collections import defaultdict, Counter

# Dataset— NLTK Gutenberg Corpus (Option A)"""
nltk.download('gutenberg', quiet=True)
from nltk.corpus import gutenberg
raw_text = gutenberg.raw('shakespeare-caesar.txt')

# Step 1 — Preprocessing
def preprocess(text: str) -> list[str]:
    """Returns lowercase word tokens with no punctuation."""
    text = text.lower()
    text = re.sub(r'[^a-z\s]', ' ', text)
    text = re.sub(r'\s+', ' ', text).strip()
    tokens = text.split()
    return tokens

# Step 2 — Build the Trigram Model
def build_trigram_model(tokens: list[str]) -> dict:
    """Returns trigram_counts[(w1,w2)][next_word] = count."""

    trigram_counts = defaultdict(Counter)

    for i in range(len(tokens) - 2):
        context   = (tokens[i], tokens[i + 1])
        next_word = tokens[i + 2]
        trigram_counts[context][next_word] += 1

    return trigram_counts

# Step 3 — Laplace Smoothing"""
def laplace_smoothing(trigram_counts: dict, vocab_size: int) -> dict:
    """Returns smoothed_probs[(w1,w2)][next_word] = probability."""

    smoothed_probs = defaultdict(dict)
    for context, word_counts in trigram_counts.items():
        context_total = sum(word_counts.values())
        denominator   = context_total + vocab_size

        for word, count in word_counts.items():
            smoothed_probs[context][word] = (count + 1) / denominator

        smoothed_probs[context]['__unk__'] = 1 / denominator

    return smoothed_probs


#  Step 4 — Text Generation
def generate_text(seed: list[str], smoothed_probs: dict,
                  vocab: list[str], num_words: int = 30) -> str:
    """Returns a generated story string of num_words words."""
    generated = seed[:]

    while len(generated) < num_words:
        context = (generated[-2], generated[-1])
        if context in smoothed_probs:
            candidates = {
                w: p
                for w, p in smoothed_probs[context].items()
                if not w.startswith('__')
            }
            next_word = max(candidates, key=candidates.get)
        else:
            next_word = random.choice(vocab)

        generated.append(next_word)

    return ' '.join(generated)


#  Step 5 — Perplexity Evaluation
def compute_perplexity(test_tokens: list[str], smoothed_probs: dict,
                       vocab_size: int) -> float:
    """Returns perplexity as a float."""

    if len(test_tokens) < 3:
        return float('inf')

    log_sum       = 0.0
    n_predictions = len(test_tokens) - 2

    for i in range(2, len(test_tokens)):
        context = (test_tokens[i - 2], test_tokens[i - 1])
        word    = test_tokens[i]

        if context in smoothed_probs:
            prob = smoothed_probs[context].get(
                word,
                smoothed_probs[context].get('__unk__', 1 / vocab_size)
            )
        else:
            prob = 1 / vocab_size

        prob = max(prob, 1e-10)
        log_sum += math.log(prob)
    perplexity = math.exp(-log_sum / n_predictions)
    return perplexity


#  Bonus — Simple Linear Interpolation (SLI)
def build_unigram_model(tokens: list[str]) -> dict:
    """Returns unigram_probs[word] = MLE probability."""
    total  = len(tokens)
    counts = Counter(tokens)
    return {word: cnt / total for word, cnt in counts.items()}


def build_bigram_model(tokens: list[str]) -> dict:
    """Returns bigram_probs[w1][w2] = MLE probability."""
    raw_counts = defaultdict(Counter)

    for i in range(len(tokens) - 1):
        raw_counts[tokens[i]][tokens[i + 1]] += 1

    bigram_probs = defaultdict(dict)
    for w1, continuations in raw_counts.items():
        total = sum(continuations.values())
        for w2, cnt in continuations.items():
            bigram_probs[w1][w2] = cnt / total

    return bigram_probs

def interpolated_probability(w1: str, w2: str, w3: str,
                              trigram_counts: dict,
                              bigram_probs: dict,
                              unigram_probs: dict,
                              lambda3: float = 0.6,
                              lambda2: float = 0.3,
                              lambda1: float = 0.1) -> float:

    context   = (w1, w2)
    tri_total = sum(trigram_counts[context].values()) if context in trigram_counts else 0
    p_tri     = (trigram_counts[context][w3] / tri_total
                 if tri_total > 0 and w3 in trigram_counts.get(context, {})
                 else 0.0)

    p_bi = bigram_probs.get(w2, {}).get(w3, 0.0)
    p_uni = unigram_probs.get(w3, 1e-10)
    return lambda3 * p_tri + lambda2 * p_bi + lambda1 * p_uni

def _generate_text_sli(seed: list[str],
                        trigram_counts: dict,
                        bigram_probs: dict,
                        unigram_probs: dict,
                        vocab: list[str],
                        num_words: int = 30) -> str:
    """Greedy 30-word generation using SLI probabilities."""
    generated = seed[:]

    while len(generated) < num_words:
        w1, w2  = generated[-2], generated[-1]
        context = (w1, w2)

        if context in trigram_counts:
            candidates = list(trigram_counts[context].keys())
        else:
            candidates = random.sample(vocab, min(300, len(vocab)))

        next_word = max(
            candidates,
            key=lambda w: interpolated_probability(
                w1, w2, w,
                trigram_counts, bigram_probs, unigram_probs
            )
        )
        generated.append(next_word)

    return ' '.join(generated)

def _compute_perplexity_sli(test_tokens: list[str],
                             trigram_counts: dict,
                             bigram_probs: dict,
                             unigram_probs: dict) -> float:
    """Perplexity on test_tokens using SLI probabilities."""
    if len(test_tokens) < 3:
        return float('inf')

    log_sum       = 0.0
    n_predictions = len(test_tokens) - 2

    for i in range(2, len(test_tokens)):
        prob = interpolated_probability(
            test_tokens[i - 2], test_tokens[i - 1], test_tokens[i],
            trigram_counts, bigram_probs, unigram_probs
        )
        prob = max(prob, 1e-10)
        log_sum += math.log(prob)

    return math.exp(-log_sum / n_predictions)

